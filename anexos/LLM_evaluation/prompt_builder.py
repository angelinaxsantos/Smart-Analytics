def build_system_prompt(rules_json_str: str) -> str:
    """
    Constrói o system prompt com fórmulas, nota sobre ruído e exemplos few-shot.
    """
    return """You are a scoring calculator for a mental health research dataset about elderly people.

Your job is to predict PHQ-9 and GAD-7 scores using the formula below as a starting point,
then adjust based on the noise information and the real examples provided.

=== VARIABLE ENCODINGS ===
gender:         1=Male, 2=Female
marital_status: 1=Single, 2=Married, 3=Divorced, 4=Widowed

=== PHQ-9 FORMULA ===
Step 1 — Define binary dummies:
  female_dummy   = 1 if gender == 2, else 0
  widowed_dummy  = 1 if marital_status == 4, else 0
  divorced_dummy = 1 if marital_status == 3, else 0

Step 2 — Compute the expected PHQ-9 score:
  phq9_raw = 5.0
            + (0.40 × comorbidities_count)
            - (0.003 × physical_activity_total_minutes_week)
            - (0.12 × sleep_hours)
            + (0.80 × female_dummy)
            - (0.012 × (monthly_income / 100))
            + (1.00 × widowed_dummy)
            + (0.50 × divorced_dummy)

Step 3 — Round to nearest integer, clip to [0, 27]:
  phq9_total = clip(round(phq9_raw), 0, 27)

=== GAD-7 FORMULA ===
Step 1 — Use the same female_dummy as above.
Step 2 — Use phq9_raw (unclipped) as raw_phq9.
Step 3 — Compute the expected GAD-7 score:
  gad7_raw = 4.0
            + (0.35 × comorbidities_count)
            - (0.002 × physical_activity_total_minutes_week)
            - (0.09 × sleep_hours)
            + (0.60 × female_dummy)
            + (0.02 × raw_phq9)

Step 4 — Round to nearest integer, clip to [0, 21]:
  gad7_total = clip(round(gad7_raw), 0, 21)

=== IMPORTANT: STOCHASTIC NOISE ===
The real scores in this dataset were NOT generated by the formula alone.
A large random noise term was added to each score during generation:
  - PHQ-9 noise: Normal(0, 5.0) — real score can differ from formula by up to ±10 points
  - GAD-7 noise: Normal(0, 6.0) — real score can differ from formula by up to ±12 points

This means:
  - Participants with many risk factors may still have LOW scores (negative noise)
  - Participants with few risk factors may still have HIGH scores (positive noise)
  - The formula gives the expected average, but real scores vary widely around it
  - Use the examples below to calibrate the magnitude of this variation

=== REAL EXAMPLES FROM THE DATASET ===
Use these real examples to understand how scores relate to profiles:

Example 1 (Mínima):
  age=65, gender=Male, marital=Single, comorbidities=1,
  activity_minutes_week=364, sleep=7.9, income=€950.15
  → PHQ9: 0 | GAD7: 0

Example 2 (Leve):
  age=73, gender=Female, marital=Married, comorbidities=3,
  activity_minutes_week=234, sleep=7.1, income=€3100.30
  → PHQ9: 5 | GAD7: 4

Example 3 (Moderada):
  age=66, gender=Female, marital=Single, comorbidities=2,
  activity_minutes_week=90, sleep=7.5, income=€3500.50
  → PHQ9: 11 | GAD7: 7

Example 4 (Mod. grave):
  age=79, gender=Male, marital=Widowed, comorbidities=3,
  activity_minutes_week=40, sleep=6.0, income=€3800.75
  → PHQ9: 18 | GAD7: 12

Example 5 (Grave):
  age=83, gender=Female, marital=Married, comorbidities=6,
  activity_minutes_week=0, sleep=4.6, income=€4100.50
  → PHQ9: 23 | GAD7: 19

=== CLASSIFICATION ===
PHQ-9:
  0–4   → Mínima
  5–9   → Leve
  10–14 → Moderada
  15–19 → Mod. grave
  20–27 → Grave

GAD-7:
  0–4   → Mínima
  5–9   → Leve
  10–14 → Moderada
  15–21 → Grave

=== OUTPUT FORMAT (STRICT) ===
Respond with EXACTLY these 4 lines and nothing else:
PHQ9: <integer>
PHQ9_CLASS: <class>
GAD7: <integer>
GAD7_CLASS: <class>

No explanations. No reasoning. No extra text. Only those 4 lines.
"""


def row_to_human_readable(row: dict) -> str:
    """Converte uma linha do CSV num texto legível para o LLM."""
    def safe_int(val):
        try:
            return str(int(float(val)))
        except (ValueError, TypeError):
            return str(val)

    def safe_float(val, decimals=1):
        try:
            return f"{float(val):.{decimals}f}"
        except (ValueError, TypeError):
            return str(val)

    gender_str  = GENDER_MAP.get(safe_int(row.get("gender", "")), row.get("gender", ""))
    marital_str = MARITAL_MAP.get(safe_int(row.get("marital_status", "")), row.get("marital_status", ""))
    living_str  = LIVING_MAP.get(safe_int(row.get("living_situation", "")), row.get("living_situation", ""))
    pa_type_str = PA_TYPE_MAP.get(safe_int(row.get("physical_activity_type", "")), row.get("physical_activity_type", ""))
    pa_int_str  = PA_INT_MAP.get(safe_int(row.get("physical_activity_intensity", "")), row.get("physical_activity_intensity", ""))

    return f"""Participant profile:
- Age: {safe_int(row.get('age'))} years
- Gender: {gender_str}
- Education: {safe_int(row.get('education_years'))} years
- Monthly income: €{safe_float(row.get('monthly_income'), 2)}
- Marital status: {marital_str}
- Living situation: {living_str}
- Comorbidities count: {safe_int(row.get('comorbidities_count'))}
- Physical activity days/week: {safe_int(row.get('physical_activity_days_per_week'))}
- Physical activity minutes/session: {safe_int(row.get('physical_activity_minutes_per_session'))}
- Total weekly activity minutes: {safe_int(row.get('physical_activity_total_minutes_week'))}
- Activity type: {pa_type_str}
- Activity intensity: {pa_int_str}
- Sleep hours/night: {safe_float(row.get('sleep_hours'))}

Based on the generation rules provided, predict the PHQ-9 and GAD-7 scores for this participant."""


def build_user_prompt(row: dict) -> str:
    return row_to_human_readable(row)


# ──────────────────────────────────────────────
# Parse da resposta do LLM
# ──────────────────────────────────────────────

def parse_llm_response(response_text: str) -> dict | None:
    """
    Faz parse da resposta do LLM.
    Espera exactamente:
        PHQ9: <int>
        PHQ9_CLASS: <str>
        GAD7: <int>
        GAD7_CLASS: <str>
    Devolve dict ou None se não conseguir fazer parse.
    """
    result = {}
    for line in response_text.strip().splitlines():
        line = line.strip()
        if line.startswith("PHQ9:") and "PHQ9_CLASS" not in line:
            try:
                result["phq9_pred"] = int(line.split(":", 1)[1].strip())
            except ValueError:
                pass
        elif line.startswith("PHQ9_CLASS:"):
            result["phq9_class_pred"] = line.split(":", 1)[1].strip()
        elif line.startswith("GAD7:") and "GAD7_CLASS" not in line:
            try:
                result["gad7_pred"] = int(line.split(":", 1)[1].strip())
            except ValueError:
                pass
        elif line.startswith("GAD7_CLASS:"):
            result["gad7_class_pred"] = line.split(":", 1)[1].strip()

    if len(result) == 4:
        return result
    return None