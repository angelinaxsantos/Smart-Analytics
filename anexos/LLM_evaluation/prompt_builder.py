"""
prompt_builder.py
Constrói o system prompt (regras + contexto) e os user prompts (instância a instância)
para prever phq9_total, phq9_classe, gad7_total e gad7_classe.
"""

import json
import csv
import io

# ──────────────────────────────────────────────
# Mapeamentos clínicos (derivados do JSON)
# ──────────────────────────────────────────────
PHQ9_CLASSES = [
    (0,  4,  "Mínima"),
    (5,  9,  "Leve"),
    (10, 14, "Moderada"),
    (15, 19, "Mod. grave"),
    (20, 27, "Grave"),
]

GAD7_CLASSES = [
    (0,  4,  "Mínima"),
    (5,  9,  "Leve"),
    (10, 14, "Moderada"),
    (15, 21, "Grave"),
]

GENDER_MAP    = {"1": "Male", "2": "Female"}
MARITAL_MAP   = {"1": "Single", "2": "Married", "3": "Divorced", "4": "Widowed"}
LIVING_MAP    = {"1": "Alone", "2": "With partner", "3": "With family", "4": "Institution"}
PA_TYPE_MAP   = {"0": "None", "1": "Walking", "2": "Light (yoga/stretching)",
                 "3": "Household/gardening", "4": "Moderate exercise", "5": "Vigorous exercise"}
PA_INT_MAP    = {"0": "None", "1": "Light", "2": "Moderate", "3": "Vigorous"}


def get_phq9_class(score: int) -> str:
    for lo, hi, label in PHQ9_CLASSES:
        if lo <= score <= hi:
            return label
    return "Unknown"


def get_gad7_class(score: int) -> str:
    for lo, hi, label in GAD7_CLASSES:
        if lo <= score <= hi:
            return label
    return "Unknown"


# ──────────────────────────────────────────────
# Carregamento de dados
# ──────────────────────────────────────────────

def load_rules(rules_path: str) -> str:
    """Carrega o JSON de regras e devolve como texto formatado."""
    with open(rules_path, "r", encoding="utf-8") as f:
        rules = json.load(f)
    return json.dumps(rules, indent=2, ensure_ascii=False)


def load_dataset(csv_path: str) -> list[dict]:
    """Carrega o CSV (separador ;) e devolve lista de dicionários."""
    rows = []
    with open(csv_path, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f, delimiter=";")
        for row in reader:
            rows.append(row)
    return rows


